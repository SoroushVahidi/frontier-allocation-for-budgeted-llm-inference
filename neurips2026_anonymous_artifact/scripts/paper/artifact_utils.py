def budget_buckets(budgets: list[int]) -> dict[int, str]:
    uniq = sorted(set(budgets))
    if not uniq:
        return {}
    if len(uniq) == 1:
        return {uniq[0]: "single"}
    if len(uniq) == 2:
        return {uniq[0]: "low", uniq[1]: "high"}
    mid = uniq[len(uniq) // 2]
    return {uniq[0]: "low", mid: "medium", uniq[-1]: "high"}


def allocation_diversity_from_oracle(
    per_example_rows: list[dict[str, str]], method_pool: list[str]
) -> list[dict[str, Any]]:
    grouped: dict[tuple[str, str, str], list[dict[str, str]]] = {}
    for r in per_example_rows:
        key = (r["dataset"], r["seed"], r["budget"])
        grouped.setdefault(key, []).append(r)

    out: list[dict[str, Any]] = []
    for (dataset, seed, budget), rows in sorted(grouped.items()):
        by_ex: dict[str, list[dict[str, str]]] = {}
        for r in rows:
            if r["method"] in method_pool:
                by_ex.setdefault(r["example_id"], []).append(r)
        if not by_ex:
            continue

        method_share = {m: 0.0 for m in method_pool}
        oracle_hits = 0
        per_method_correct = {m: 0 for m in method_pool}

        for ex_rows in by_ex.values():
            correct = [r for r in ex_rows if str(r.get("is_correct", "")).lower() in {"1", "true"}]
            if correct:
                oracle_hits += 1
                winners = [r["method"] for r in correct]
            else:
                min_act = min(as_float(r.get("actions_used", 9999.0)) for r in ex_rows)
                winners = [r["method"] for r in ex_rows if as_float(r.get("actions_used", 9999.0)) == min_act]
            frac = 1.0 / max(1, len(winners))
            for m in winners:
                method_share[m] += frac

            for m in method_pool:
                mr = next((r for r in ex_rows if r["method"] == m), None)
                if mr and str(mr.get("is_correct", "")).lower() in {"1", "true"}:
                    per_method_correct[m] += 1

        n_ex = len(by_ex)
        probs = [method_share[m] / n_ex for m in method_pool]
        entropy = -sum(p * math.log(p) for p in probs if p > 0)
        max_entropy = math.log(len(method_pool)) if len(method_pool) > 1 else 1.0
        normalized_entropy = (entropy / max_entropy) if max_entropy > 0 else 0.0
        max_share = max(probs) if probs else 0.0
        n_active = sum(1 for p in probs if p > 1e-12)

        out.append(
            {
                "dataset": dataset,
                "seed": int(seed),
                "budget": int(budget),
                "n_examples": n_ex,
                "method_pool_size": len(method_pool),
                "allocation_entropy": normalized_entropy,
                "max_family_share": max_share,
                "n_active_families": n_active,
                "family_coverage": n_active / max(1, len(method_pool)),
                "oracle_frontier_accuracy": oracle_hits / max(1, n_ex),
                "best_single_family_reference_accuracy": max((c / max(1, n_ex) for c in per_method_correct.values()), default=0.0),
            }
        )
    return out


def mean_std(values: list[float]) -> tuple[float, float]:
    if not values:
        return (0.0, 0.0)
    if len(values) == 1:
        return (values[0], 0.0)
    return (mean(values), pstdev(values))
