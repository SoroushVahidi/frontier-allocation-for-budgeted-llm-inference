def _fail(msg: str) -> None:
    raise FileNotFoundError(msg)


def _latest_run_dir(root: Path, required_files: set[str]) -> Path:
    if not root.exists():
        _fail(f"Missing canonical root directory: {root}")
    run_dirs = sorted([p for p in root.iterdir() if p.is_dir()])
    if not run_dirs:
        _fail(f"No run directories found under {root}")
    valid = []
    for run in run_dirs:
        missing = [f for f in required_files if not (run / f).exists()]
        if not missing:
            valid.append(run)
    if not valid:
        _fail(f"No valid run directory under {root} contains required files: {sorted(required_files)}")
    return valid[-1]


def canonical_paths() -> dict[str, Path]:
    imported_run = _latest_run_dir(IMPORTED_ROOT, REQUIRED_IMPORTED_FILES)
    full_bundle_run = _latest_run_dir(FULL_BUNDLE_ROOT, REQUIRED_FULL_BUNDLE_FILES)
    return {
        "imported_run": imported_run,
        "full_bundle_run": full_bundle_run,
    }


